    /// Create by Mr.Cyber
	///<summarly>
    /// Style of page animation for appearing/disappearing
    ///</summarly>
    public enum PageAnimation
    {

        /// <summary>
        /// No animation take place 
        /// </summary>
        None = 0,

        /// <summary>
        /// The page slides in and fades in from the right
        /// </summary>
        SlideAndFadeInFromRight = 1,

        /// <summary>
        /// The page slides out anf fades out to the left
        /// </summary>
        SlideAndFadeOutFromLeft = 2,
    }
}